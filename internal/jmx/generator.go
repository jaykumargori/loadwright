func writeHTTPSampler(b *strings.Builder, s *spec.Spec, r spec.Request) {
	target, _ := url.Parse(s.Target)
	fmt.Fprintf(b, `        <HTTPSamplerProxy guiclass="HttpTestSampleGui" testclass="HTTPSamplerProxy" testname="%s" enabled="true">`+"\n", esc(r.Name))
	body := rawBodyString(r)
	multipart := len(r.BodyMultipart) > 0
	if body != "" {
		b.WriteString("          <boolProp name=\"HTTPSampler.postBodyRaw\">true</boolProp>\n")
	} else {
		b.WriteString("          <boolProp name=\"HTTPSampler.postBodyRaw\">false</boolProp>\n")
	}
	b.WriteString("          <elementProp name=\"HTTPsampler.Arguments\" elementType=\"Arguments\" guiclass=\"HTTPArgumentsPanel\" testclass=\"Arguments\" testname=\"User Defined Variables\" enabled=\"true\">\n")
	b.WriteString("            <collectionProp name=\"Arguments.arguments\">\n")
	if body != "" {
		b.WriteString("              <elementProp name=\"\" elementType=\"HTTPArgument\">\n")
		b.WriteString("                <boolProp name=\"HTTPArgument.always_encode\">false</boolProp>\n")
		fmt.Fprintf(b, "                <stringProp name=\"Argument.value\">%s</stringProp>\n", esc(body))
		b.WriteString("                <stringProp name=\"Argument.metadata\">=</stringProp>\n")
		b.WriteString("              </elementProp>\n")
	} else if multipart {
		writeMultipartTextArguments(b, r.BodyMultipart)
	} else if len(r.BodyForm) > 0 {
		writeFormArguments(b, r.BodyForm)
	} else {
		writeQueryArguments(b, r.Query)
	}
	b.WriteString("            </collectionProp>\n")
	b.WriteString("          </elementProp>\n")
	if multipart {
		writeMultipartFileArguments(b, r.BodyMultipart)
	}
	fmt.Fprintf(b, "          <stringProp name=\"HTTPSampler.domain\">%s</stringProp>\n", esc(target.Hostname()))
	fmt.Fprintf(b, "          <stringProp name=\"HTTPSampler.port\">%s</stringProp>\n", esc(target.Port()))
	fmt.Fprintf(b, "          <stringProp name=\"HTTPSampler.protocol\">%s</stringProp>\n", esc(target.Scheme))
	b.WriteString("          <stringProp name=\"HTTPSampler.contentEncoding\">UTF-8</stringProp>\n")
	fmt.Fprintf(b, "          <stringProp name=\"HTTPSampler.path\">%s</stringProp>\n", esc(r.Path))
	fmt.Fprintf(b, "          <stringProp name=\"HTTPSampler.method\">%s</stringProp>\n", esc(r.Method))
	b.WriteString("          <boolProp name=\"HTTPSampler.follow_redirects\">true</boolProp>\n")
	b.WriteString("          <boolProp name=\"HTTPSampler.auto_redirects\">false</boolProp>\n")
	b.WriteString("          <boolProp name=\"HTTPSampler.use_keepalive\">true</boolProp>\n")
	fmt.Fprintf(b, "          <boolProp name=\"HTTPSampler.DO_MULTIPART_POST\">%s</boolProp>\n", boolLiteral(multipart))
	b.WriteString("          <stringProp name=\"HTTPSampler.embedded_url_re\"></stringProp>\n")
	timeoutMS := ""
	if r.Timeout.Set {
		timeoutMS = fmt.Sprintf("%d", r.Timeout.Seconds*1000)
	}
	fmt.Fprintf(b, "          <stringProp name=\"HTTPSampler.connect_timeout\">%s</stringProp>\n", timeoutMS)
	fmt.Fprintf(b, "          <stringProp name=\"HTTPSampler.response_timeout\">%s</stringProp>\n", timeoutMS)
	b.WriteString("        </HTTPSamplerProxy>\n")
	b.WriteString("        <hashTree>\n")
	headers := requestHeaders(r)
	if len(headers) > 0 {
		writeHeaders(b, headers)
	}
	if r.Expect.Status > 0 {
		writeStatusAssertion(b, r.Expect.Status)
	}
	b.WriteString("        </hashTree>\n")
}

func writeWebSocketSampler(b *strings.Builder, s *spec.Spec, r spec.Request) {
	wsURL := r.WebSocket.URL
	if wsURL == "" {
		base := strings.TrimRight(s.Target, "/")
		path := r.Path
		if path == "" {
			path = "/"
		}
		wsURL = base + path
	}

	server, port, wsPath, useTLS := parseWebSocketURL(wsURL)
	connectTimeoutMs := 20000
	if r.WebSocket.Timeout.Set {
		connectTimeoutMs = r.WebSocket.Timeout.Seconds * 1000
	}
	readTimeoutMs := connectTimeoutMs
	closeTimeoutMs := readTimeoutMs
	if r.WebSocket.CloseTimeout.Set {
		closeTimeoutMs = r.WebSocket.CloseTimeout.Seconds * 1000
	}
	handshakeHeaders := websocketHandshakeHeaders(r.WebSocket)
	messages := r.WebSocket.Messages

	// Single request-response: one send with one expect.
	if len(messages) == 1 && messages[0].Expect != nil {
		if messages[0].Expect.Timeout.Set {
			readTimeoutMs = messages[0].Expect.Timeout.Seconds * 1000
		}
		payloadType := "Text"
		if messages[0].Type == "binary" {
			payloadType = "Binary"
		}
		writeRequestResponseSampler(b, r.Name, server, port, wsPath, useTLS, connectTimeoutMs, readTimeoutMs, messages[0].Send, payloadType, true)
		writeRequestResponseChildren(b, handshakeHeaders, &messages[0])
		return
	}

	// Multi-message: Open + Write/Read pairs + Close, wrapped in a Transaction Controller.
	fmt.Fprintf(b, `        <TransactionController guiclass="TransactionControllerGui" testclass="TransactionController" testname="%s" enabled="true">`+"\n", esc(r.Name))
	b.WriteString("          <boolProp name=\"TransactionController.includeTimers\">false</boolProp>\n")
	b.WriteString("          <boolProp name=\"TransactionController.parent\">true</boolProp>\n")
	b.WriteString("        </TransactionController>\n")
	b.WriteString("        <hashTree>\n")

	// Open connection.
	writeOpenConnectionSampler(b, r.Name+" - open", server, port, wsPath, useTLS, connectTimeoutMs, readTimeoutMs, handshakeHeaders)

	// Send/Read messages.
	for i, msg := range messages {
		if msg.Delay.Set && msg.Delay.Seconds > 0 {
			writeConstantTimer(b, msg.Delay.Seconds*1000)
		}
		msgReadTimeout := readTimeoutMs
		if msg.Expect != nil && msg.Expect.Timeout.Set {
			msgReadTimeout = msg.Expect.Timeout.Seconds * 1000
		}
		payloadType := "Text"
		if msg.Type == "binary" {
			payloadType = "Binary"
		}

		if msg.Expect != nil {
			// Send and expect response in one sampler (reuse connection from Open).
			writeRequestResponseSampler(b, fmt.Sprintf("%s - msg %d", r.Name, i), server, port, wsPath, useTLS, connectTimeoutMs, msgReadTimeout, msg.Send, payloadType, false)
			writeRequestResponseChildren(b, nil, &msg)
		} else {
			// Fire-and-forget write.
			writeSingleWriteSampler(b, fmt.Sprintf("%s - send %d", r.Name, i), connectTimeoutMs, msgReadTimeout, msg.Send, payloadType)
		}
	}

	// Close connection.
	writeCloseSampler(b, r.Name+" - close", closeTimeoutMs)

	b.WriteString("        </hashTree>\n")
}

// parseWebSocketURL extracts server, port, path, and TLS from a ws:// or wss:// URL.
func parseWebSocketURL(rawURL string) (server string, port string, path string, useTLS bool) {
	useTLS = strings.HasPrefix(rawURL, "wss://")
	cleaned := strings.TrimPrefix(strings.TrimPrefix(rawURL, "wss://"), "ws://")
	parts := strings.SplitN(cleaned, "/", 2)
	hostPort := parts[0]
	path = "/"
	if len(parts) > 1 {
		path = "/" + parts[1]
	}
	if strings.Contains(hostPort, ":") {
		hp := strings.SplitN(hostPort, ":", 2)
		server = hp[0]
		port = hp[1]
	} else {
		server = hostPort
		if useTLS {
			port = "443"
		} else {
			port = "80"
		}
	}
	return
}

