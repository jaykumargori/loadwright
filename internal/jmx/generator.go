func rawBodyString(r spec.Request) string {
	if r.BodyText != "" {
		return r.BodyText
	}
	if r.BodyJSON != nil {
		return bodyString(r.BodyJSON)
	}
	return bodyString(r.Body)
}

func bodyString(body any) string {
	if body == nil {
		return ""
	}
	if value, ok := body.(string); ok {
		return value
	}
	data, err := json.Marshal(jsonable(body))
	if err != nil {
		return fmt.Sprintf("%v", body)
	}
	return string(data)
}

func jsonable(value any) any {
	switch typed := value.(type) {
	case map[string]any:
		out := map[string]any{}
		for key, item := range typed {
			out[key] = jsonable(item)
		}
		return out
	case map[any]any:
		out := map[string]any{}
		for key, item := range typed {
			out[fmt.Sprintf("%v", key)] = jsonable(item)
		}
		return out
	case []any:
		out := make([]any, len(typed))
		for index, item := range typed {
			out[index] = jsonable(item)
		}
		return out
	default:
		return value
	}
}

func requestHeaders(r spec.Request) map[string]string {
	if len(r.BodyMultipart) > 0 {
		headers := map[string]string{}
		for key, value := range r.Headers {
			if strings.EqualFold(key, "content-type") {
				continue
			}
			headers[key] = value
		}
		return headers
	}
	if len(r.BodyForm) == 0 || hasHeader(r.Headers, "content-type") {
		return r.Headers
	}
	headers := map[string]string{}
	for key, value := range r.Headers {
		headers[key] = value
	}
	headers["Content-Type"] = "application/x-www-form-urlencoded"
	return headers
}

func boolLiteral(value bool) string {
	if value {
		return "true"
	}
	return "false"
}

func hasHeader(headers map[string]string, name string) bool {
	for key := range headers {
		if strings.EqualFold(key, name) {
			return true
		}
	}
	return false
}

func esc(value string) string {
	var b bytes.Buffer
	_ = xml.EscapeText(&b, []byte(value))
	return b.String()
}

func sortedKeys(values map[string]string) []string {
	keys := make([]string, 0, len(values))
	for key := range values {
		keys = append(keys, key)
	}
	sort.Strings(keys)
	return keys
}

func sortedDataSetNames(values map[string]spec.DataSet) []string {
	keys := make([]string, 0, len(values))
	for key := range values {
		keys = append(keys, key)
	}
	sort.Strings(keys)
	return keys
}

func boolString(value *bool, defaultValue bool) string {
	if value == nil {
		if defaultValue {
			return "true"
		}
		return "false"
	}
	if *value {
		return "true"
	}
	return "false"
}

func shareMode(value string) string {
	switch value {
	case "thread":
		return "Current thread"
	case "group":
		return "Current thread group"
	default:
		return "All threads"
	}
}
