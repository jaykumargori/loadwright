func SafeName(value string) string {
	re := regexp.MustCompile(`[^A-Za-z0-9_.-]+`)
	cleaned := strings.Trim(re.ReplaceAllString(strings.TrimSpace(value), "-"), "-")
	if cleaned == "" {
		return "test"
	}
	return cleaned
}

func Compile(s *spec.Spec, outputPath string) error {
	if err := os.MkdirAll(filepath.Dir(outputPath), 0o755); err != nil {
		return err
	}
	return os.WriteFile(outputPath, []byte(Render(s)), 0o644)
}

func Render(s *spec.Spec) string {
	var b strings.Builder
	b.WriteString(`<?xml version="1.0" encoding="UTF-8"?>` + "\n")
	b.WriteString(`<jmeterTestPlan version="1.2" properties="5.0" jmeter="5.6.3">` + "\n")
	b.WriteString("  <hashTree>\n")
	writeTestPlan(&b, s)
	b.WriteString("    <hashTree>\n")
	writeThreadGroup(&b, s)
	b.WriteString("      <hashTree>\n")
	writeDataSets(&b, s)
	for _, request := range s.Requests {
		if request.Protocol == "websocket" {
			writeWebSocketSampler(&b, s, request)
			continue
		}
		writeHTTPSampler(&b, s, request)
	}
	b.WriteString("      </hashTree>\n")
	b.WriteString("    </hashTree>\n")
	b.WriteString("  </hashTree>\n")
	b.WriteString("</jmeterTestPlan>\n")
	return b.String()
}

